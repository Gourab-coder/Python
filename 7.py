# List (use case: Backend, ML, DSA)
nums = [10, 20, 7, 30]
print(nums)

# loops
for x in nums:
    print(x)

# append
nums.append(40)
print(nums)

nums.append(4)
print(nums)

# pop
nums.pop()
print(nums)

# remove
nums.remove(7)
print(nums)

# insert
nums.insert(1, 6)
print(nums)

# sort (only work for same data type in a list)
nums.sort()
print(nums)

# reverse
nums.reverse()
print(nums)

# len
print("size of nums is ",len(nums))

data = [10, "Python", 3.14, True, 7]
print(data)
print(type(data))

data.reverse()
print(data)